def panel_settings_help_page_count() -> int:
    return len(PANEL_SETTINGS_HELP_PAGES)


def build_panel_settings_help_text(panel: Any, page: int) -> str:
    total = panel_settings_help_page_count()
    page = max(0, min(page, total - 1))
    body = PANEL_SETTINGS_HELP_PAGES[page]
    header = f"🧷 پنل: **{panel.name}** — `کد {panel.code}`\n\n"
    return header + body


def build_panel_settings_help_buttons(panel_code: int | str, page: int) -> list:
    total = panel_settings_help_page_count()
    page = max(0, min(page, total - 1))
    rows: list = []

    nav: list = []
    if page > 0:
        nav.append(glass_inline_button("◀️ صفحه قبل", data=f"panel_settings_help:{panel_code}:{page - 1}"))
    if page < total - 1:
        nav.append(glass_inline_button("صفحه بعد ▶️", data=f"panel_settings_help:{panel_code}:{page + 1}"))
    if nav:
        rows.append(nav)

    rows.append([glass_inline_button("🔙 بازگشت به تنظیمات پنل", data=f"panel_info:{panel_code}")])
    return rows
